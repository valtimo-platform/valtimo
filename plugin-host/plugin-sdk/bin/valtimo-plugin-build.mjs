/**
 * valtimo-plugin-build
 *
 * Compiles a plugin's TypeScript source to a .wasm module using extism-js.
 *
 * Steps:
 * 1. Bundle the plugin TS/JS source into a single JS file (via esbuild)
 * 2. Compile the bundled JS to .wasm via the extism-js CLI
 *
 * Usage: valtimo-plugin-build [--input src/plugin.ts] [--output plugin.wasm]
 */

import { execFileSync } from "node:child_process";
import { existsSync, mkdirSync, writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

// Wasm interface (`-i`) consumed by extism-js: declares the exported entrypoint and the host
// functions the module imports. This is identical for every plugin, so the build generates it
// automatically — plugin authors never need to write Wasm-specific type declarations.
const DEFAULT_INTERFACE = `declare module "main" {
  export function handle_action(): I32;
  export function handle_event(): I32;
}

declare module "extism:host" {
  interface user {
    gzac_api(input: PTR): PTR;
  }
}
`;

const args = process.argv.slice(2);
const inputIdx = args.indexOf("--input");
const outputIdx = args.indexOf("--output");

const input = inputIdx >= 0 ? args[inputIdx + 1] : "src/plugin.ts";
const output = outputIdx >= 0 ? args[outputIdx + 1] : "plugin.wasm";

const cwd = process.cwd();
const inputPath = resolve(cwd, input);
const outputPath = resolve(cwd, output);
const bundlePath = resolve(cwd, "dist", "_plugin_bundle.js");
const entryPath = resolve(cwd, "dist", "_plugin_entry.mjs");

if (!existsSync(inputPath)) {
  console.error(`Error: Input file not found: ${inputPath}`);
  process.exit(1);
}

console.log(`[valtimo-plugin-build] Bundling ${input} ...`);

// Ensure dist directory exists
mkdirSync(dirname(bundlePath), { recursive: true });

// Generate the Wasm entry. It imports the plugin module for its side effects (the `action`/
// `onEvent` registrations the author makes at module load) and re-exports the SDK's Wasm
// entrypoints. This is what lets authors write only `src/plugin.ts` — no
// `module.exports = { handle_action }` boilerplate, and no hand-wired Wasm exports.
const pluginImport = JSON.stringify(inputPath.replace(/\\/g, "/"));
writeFileSync(
  entryPath,
  `// Auto-generated by valtimo-plugin-build — do not edit.\n` +
    `import ${pluginImport};\n` +
    `export { handle_action, handle_event } from "@valtimo/plugin-sdk";\n`
);

// Step 1: Bundle the generated entry with esbuild into a single CJS file. esbuild compiles the
// re-exports onto `module.exports`, which is what extism-js reads as the module's exports.
try {
  execFileSync("npx", [
    "esbuild", entryPath,
    "--bundle", `--outfile=${bundlePath}`,
    "--format=cjs", "--target=es2020",
    "--platform=neutral", "--main-fields=main",
    "--external:@extism/js-pdk",
  ], { cwd, stdio: "inherit" });
} catch (err) {
  console.error("[valtimo-plugin-build] esbuild bundling failed");
  process.exit(1);
}

// Step 2: Compile to .wasm via extism-js
console.log(`[valtimo-plugin-build] Compiling to WebAssembly ...`);

// Resolve extism-js binary: check PATH first, then known local locations
function findExtismJs() {
  try {
    execFileSync("extism-js", ["--version"], { stdio: "pipe" });
    return "extism-js";
  } catch {}

  // Check local .bin directory (monorepo convention)
  const localBin = resolve(cwd, "node_modules", ".bin", "extism-js");
  if (existsSync(localBin)) return localBin;

  // Check .bin directories in the monorepo
  const searchDirs = [
    resolve(__dirname, "..", "..", ".bin", "extism-js"),       // plugin-host/.bin/
    resolve(__dirname, "..", "..", "..", ".bin", "extism-js"),  // repo root .bin/
  ];
  for (const bin of searchDirs) {
    if (existsSync(bin)) return bin;
  }

  return null;
}

const extismJsBin = findExtismJs();
if (!extismJsBin) {
  console.error("[valtimo-plugin-build] extism-js CLI not found.");
  console.error("Install it from: https://github.com/extism/js-pdk/releases");
  console.error("Or place it in plugin-host/.bin/extism-js for local development.");
  process.exit(1);
}

try {
  // Use the plugin's own index.d.ts if it provides one (e.g. to declare extra host functions),
  // otherwise generate the standard interface so src/plugin.ts is the only file authors write.
  let interfaceFile = resolve(cwd, "index.d.ts");
  if (!existsSync(interfaceFile)) {
    interfaceFile = resolve(cwd, "dist", "_plugin_interface.d.ts");
    writeFileSync(interfaceFile, DEFAULT_INTERFACE);
    console.log("[valtimo-plugin-build] Using generated Wasm interface (no index.d.ts found)");
  }
  const extismArgs = [bundlePath, "-o", outputPath, "-i", interfaceFile];
  execFileSync(extismJsBin, extismArgs, { cwd, stdio: "inherit" });
  console.log(`[valtimo-plugin-build] Built: ${output}`);
} catch (err) {
  console.error("[valtimo-plugin-build] extism-js compilation failed");
  process.exit(1);
}
